def get_gpu_memory_used():
    """Return current GPU memory usage in MB (if available)."""
    try:
        import pynvml

        pynvml.nvmlInit()
        handle = pynvml.nvmlDeviceGetHandleByIndex(0)
        mem_info = pynvml.nvmlDeviceGetMemoryInfo(handle)
        return mem_info.used // 1024**2
    except Exception:
        return -1
